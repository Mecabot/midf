<h4>La participación ciudadana es una acción clave para un desarrollo humano sensible.</h4>

<p>Pasos a seguir para reportar una incidencia (Reporta,Mide,Mejora) </p>


<ul type=”1” start=”10”>
    <h2> Reporta </h2>
    <ol>
        <li>Elije la categoría a la que pertenece tu incidencia.</li>
        <li>Elije la Sub categoria en el menú emergente.</li>
        <li>Completa el formularío con la información básica para tu reporte.</li>
        <li>Envía tu solicitud, con ello obtendrás tu numero de folio.(<i>Es importante que guardes el folio, ya que con el podras darle seguimiento a tu denuncia</i>  )</li>
    </ol>
    <hr>
    <h2> Mide </h2>
    <p>Puedes saber en que estatus se encuentra tu incidencia</p>
    <ol>
        <li>Ingresa a la seccion de <b>Seguimiento</b></li>
        <li>Captura tu numero de <i>Folio</i></li>
        <li>Captura el captcha </li>
        <li>Da click en <b>Consltar</b> para ver el estatus.</i> </li>
    </ol>
    <p>También puedes ver las categorias en la parte superior del sitio.</p>
    <ol>
        <li>Da click en el menu de la categoría que deseas consultar.</li>
        <li>Elije la Sub categoria en el menú emergente.</li>
        <li>Compara los indicadores delegacionales. </li>
    </ol>
    <h2> Mejora </h2>
    <p>Cada incidencia que reportas, ayuda a Evaluar/Mejorar las agencias de gobierno.</p>
    <hr>

