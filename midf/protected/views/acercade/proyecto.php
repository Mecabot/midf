<h2><b>Objetivo General:</b></h2>

<p>Idea, diseña y desarrolla una aplicación usando los datasets liberados por las entidades del gobierno de la ciudad. Los participantes en el reto de Aplicaciones Cívicas deberán idear, diseñar y desarrollar un prototipo o producto mínimo viable (PMV) de aplicaciones web o móviles.</p>

<h2><b>MiDF</b></h2>
<p>Promueve y facilita la participación de los ciudadanos en las acciones del gobierno (reportes a través de un único medio).
    Sensibiliza al gobierno sobre los temas cotidianos que le importan a la ciudadanía y que tienen un impacto inmediato en su calidad
    de vida. Facilita la toma de decisiones y ofrece una manera de priorizar el gasto de los recursos públicos ("retweet" de los reportes).
    Permite el monitoreo de la actividad gubernamental por parte de la sociedad (seguimiento de los reportes por número de folio).
    Ofrece una herramienta para medir y comparar la eficacia y eficiencia en la resolución de problemas similares entre sucursales de
    las agencias y entre deleghttp://www.sepomex.gob.mx/lservicios/servicios/CodigoPostal_Exportar.aspxaciones (benchmarking) y ofrece la oportunidad de localizar (y adoptar) las mejores prácticas. Mejora la
    calidad de vida de los ciudadanos al resolver los problemas que realmente le importan. Le da al ciudadano un sentido de pertenencia y
    lo coloca en el centro de la acción pública.</p>

